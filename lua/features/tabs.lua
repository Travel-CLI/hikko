--[[ Настройка табов ]]--
-- Use spaces by default
opt.expandtab = true

-- Set amount of space characters, when we press "<" or ">"
opt.shiftwidth = 2

-- 1 tab equal 2 spaces
opt.tabstop = 2

-- Turn on smart indentation. See in the docs for more info
-- INFO: :help smartindent
opt.smartindent = true

